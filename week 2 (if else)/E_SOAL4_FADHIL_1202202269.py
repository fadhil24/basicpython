nomor = int(input("Masukkan nomor hari : "))

if (nomor == 1 ) :
    print(f"Hari ke- {nomor} adalah Hari Senin")
elif (nomor == 2 ) :
    print(f"Hari ke- {nomor} adalah Hari Selasa")
elif (nomor == 3 ) :
    print(f"Hari ke- {nomor} adalah Hari Rabu")
elif (nomor == 4 ) :
    print(f"Hari ke- {nomor} adalah Hari Kamis")
elif (nomor == 5 ) :
    print(f"Hari ke- {nomor} adalah Hari Jum'at")
elif (nomor == 6 ) :
    print(f"Hari ke- {nomor} adalah Hari Sabtu")
elif (nomor == 7 ) :
    print(f"Hari ke- {nomor} adalah Hari Minggu")
else :
    print(f"Tidak ada hari ke- {nomor}")